// Tipo de dados em JS operadores aritméticos

// adição
console.log('Resultado de 1 + 5:', 1 + 5)

// subtração
console.log('Resultado de 50 - 15:', 50 - 15)

// multiplicação
console.log('Resultado de 4 * 12:', 4 * 12)

// divisão
console.log('Resultado de 48 / 4:', 48 / 4)

/* Ordem dos Cálculos em JS */
console.log('Resultado de 1 + 100 * 5:', 1 + 100 * 5)

// agrupando por operador
console.log('Resultado de (1 + 100) * 5:', [1 + 100] * 5)


// operador de concatenação (+)
// "Senac" + "Piauí"
console.log("Senac" + " " +  "Piauí");
console.log("Senac" +  " Piauí");

// Operador de módulo (%)
// 12 % 5
console.log("Resultado de 12 % 5:", 12 % 5);
