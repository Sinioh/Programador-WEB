// Tipo de dados em JS operadores aritméticos
// adição
let a = 1;
let b = 5;
console.log('Resultado de a + b:', a + b);

// subtração
let x = 50;
let y = 15;
console.log('Resultado de x - y:', x - y);

// multiplicação
x = 4;
y = 12;
console.log('Resultado de x * y', x * y);

// divisão
y = 48;
console.log('Resultado de y / x:', y / x);

/* Ordem dos Cálculos em JS */
x = 1;
y = 100;
let z = 5;
console.log('Resultado de x + y * z:', x + y * z);

// agrupando por operador
console.log('Resultado de (x + y) * z:', [x + y] * z);


// operador de concatenação (+)
// "Senac" + "Piauí"
let escola = "Senac"; 
let estado = "Piauí";
let separador = " ";

console.log(escola + separador + estado)
// Operador de módulo (%)
// 12 % 5
x = 13
y = 5
console.log("Resultado de x % y:", x % y);