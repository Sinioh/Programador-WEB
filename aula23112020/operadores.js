// Operadores de atribuição composta

let num1 = 5;
let num2 = 11;

// +=

console.log('Atribuição de soma: += num2(11)', num1 += num2);
console.log('Atribuição de subtração: -= num2(11)', num1 -= num2);
console.log('Atribuição de multiplicação: *= num2(11)', num1 *= num2);
console.log('Atribuição de divisão: /= num2(11)', num1 /= num2);
console.log('Atribuição de módulo: %= num2(11)', num1 %= num2);
console.log('Atribuição de exponenciação: **= num2(11)', num1 **= num2);


